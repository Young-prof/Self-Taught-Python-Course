# Sorting is a method in python and in other programming languages that is used to arrange or organize data
print('Original List here:')
cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort()
print(cars)

'''
reverse Alfabetical Order in python
We do the reverse alfabetical order by adding reverse=True within the call back sort function
'''
print('\nSorted List here:')
cars.sort(reverse=True)
print(cars)
