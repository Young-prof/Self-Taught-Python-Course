# Removing the last Element using the pop keyword

motorcycles = ['honda', 'yamaha', 'suzuki']
print(motorcycles)

# this line is to store the popped motocycle
popped_motorcycle = motorcycles.pop()
print(motorcycles)
print(popped_motorcycle)  # To print the popped Motocycle

popped_out = f'This is the popped motocycle >> {popped_motorcycle}'
print(popped_out)
